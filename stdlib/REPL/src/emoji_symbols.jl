function emoji_data(url)
    emojis = JSON.parsefile(download(url))
    result = Dict()
    for emj in emojis
        name = "\\:" * emj["short_name"] * ":"
        unicode = emj["unified"]
        c = if '-' in unicode
            String(map(u -> Char(parse(UInt32, u, base = 16)),
                   split(unicode, '-')))
        else
            string(Char(parse(UInt32, unicode, base = 16)))
        end
        if emj["short_name"] == "satellite" || emj["name"] == "SATELLITE"
            println(url)
            println("satellite = ", c)
        else
            println(emj["short_name"], " => ", c)
        end
        result[name] = c
    end
    return result
end

