import JSON

function emoji_data(url)
    emojis = JSON.parsefile(download(url))
    result = Dict()
    for emj in emojis
        name = "\\:" * emj["short_name"] * ":"
        unicode = emj["unified"]
        s = String(map(u -> Char(parse(UInt32, u, base = 16)),
                   split(unicode, '-')))
        result[name] = s
    end
    return result
end

function merge_but_arrows!(a, b)
    for (k, v) in b
        if haskey(a, k) && occursin("arrow", k)
            continue
        end
        a[k] = v
    end
    return a
end

# We combine multiple versions as the data changes, and not only by growing.
result = mapfoldr(emoji_data, merge_but_arrows!, [
